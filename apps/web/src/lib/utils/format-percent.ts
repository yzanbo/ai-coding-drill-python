// format-percent: 0〜1 の小数を「71.4%」表記に整形する共通ヘルパ。
//
//   - 小数 1 桁固定（toFixed(1)）：揃った桁数で並べないと統計画面の縦並びが
//     読みにくい。0 桁丸めだと 99% と 99.9% が同じに見えて精度感を損なう、
//     2 桁にすると有効桁数が過剰（採点母数が小さい時に意味の無いノイズが出る）。
//   - 入力レンジは 0〜1 を想定。負数や 1 超は API 側が返さない契約のため
//     ガードしない（壊れた値は壊れた表示で観測する方が原因追跡しやすい）。
//
//   使用箇所：/me/stats, /me/weakness 等の集計系

export const formatPercent = (n: number): string => `${(n * 100).toFixed(1)}%`;
